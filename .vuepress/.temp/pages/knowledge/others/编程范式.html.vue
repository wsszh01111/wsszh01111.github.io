<template><h1 id="概念性了解下编程范式" tabindex="-1"><a class="header-anchor" href="#概念性了解下编程范式" aria-hidden="true">#</a> 概念性了解下编程范式</h1>
<p>编程范式是编程的方法论，指导我们以什么样的方式或风格去编程，也最终会影响我们对于程序执行的看待方式，如面向对象编程中，程序会被看做一系列对象之间的相互作用，而在函数式编程中，程序会被看作是一个无状态的函数计算的序列。</p>
<p>编程范式同时也会有约束要求，例如纯函数式编程不允许有副作用，大部分编程范式都要求程序是结构化的（所谓结构化的就是使用子程序、块结构、for循环以及while循环等结构，来取代传统的 goto；以此来改善计算机程序的明晰性、质量以及开发时间，并且避免写出面条式代码（程序的流向就像一盘面一样的扭曲纠结））</p>
